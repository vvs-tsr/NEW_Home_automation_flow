if not cap.isOpened():
    print(f"[Camera] ❌ Cannot open IP camera stream from {IPCAM_URL}")
    print("         Please check URL, camera app, and network access.")
    exit()

print(f"[Camera] ✅ Stream opened from {IPCAM_URL}")

# =========================
# MAIN VIDEO PROCESSING LOOP
# =========================
while True:
    ret, frame = cap.read()
    if not ret:
        print("[Camera] ⚠️ Frame not received, attempting to re-open stream...")
        cap.release()
        time.sleep(2)
        cap = cv2.VideoCapture(IPCAM_URL, cv2.CAP_FFMPEG)
        cap.set(cv2.CAP_PROP_BUFFERSIZE, BUFFER_SIZE)
        time.sleep(1)
        if not cap.isOpened():
            print("[Camera] ❌ Failed to re-open stream. Exiting.")
            break
        else:
            print("[Camera] ✅ Stream re-opened successfully.")
            continue

    # =========================
    # YOLO INFERENCE
    # =========================
    try:
        # Perform inference on the original frame
        results = model(frame, conf=CONF_THRESHOLD, verbose=False) # verbose=False reduces terminal output
    except Exception as e:
        print(f"[YOLO] ⚠️ Error during inference: {e}. Skipping frame.")
        continue

    # Create a copy for annotation. We only annotate if security objects are found.
    annotated_frame = frame.copy()
    current_detections_for_mqtt = [] # List to store unique detections for this frame, ready for MQTT

    found_security_object = False # Flag to check if any security object was detected

    for r in results:
        boxes = r.boxes
        for box in boxes:
            cls_id = int(box.cls[0])
            label = model.names[cls_id]
            conf = float(box.conf[0])

            # --- ONLY PROCESS IF THE OBJECT IS IN THE SECURITY_CLASSES LIST ---
            if label in SECURITY_CLASSES:
                found_security_object = True
                x1, y1, x2, y2 = map(int, box.xyxy[0])

                # Draw bounding box on the annotated frame
                cv2.rectangle(annotated_frame, (x1, y1), (x2, y2), (255,0, 255), 2)
                cv2.putText(
                    annotated_frame,
                    f"{label} {conf:.2f}",
                    (x1, y1 - 8),
                    cv2.FONT_HERSHEY_SIMPLEX,
                    0.5,
                    (255, 0, 0),
                    2
                )

                # Check for debouncing for this specific label
                now = datetime.now()
                if label not in last_alert_times or (now - last_alert_times[label]).total_seconds() > ALERT_DEBOUNCE_TIME:
                    current_detections_for_mqtt.append({
                        "label": label,
                        "confidence": round(conf, 2),
                        "timestamp": now.isoformat()
                    })
                    last_alert_times[label] = now # Update last alert time for this label

    # =========================
    # MQTT Publishes (Send only if unique new security detections were found)
    # =========================
    if current_detections_for_mqtt:
        # Human-readable payload
        alert_message_parts = []
        for d in current_detections_for_mqtt:
            alert_message_parts.append(f"{d['label']} ({(d['confidence']*100):.0f}%)")
        human_readable_payload = f"SECURITY ALERT: Detected {', '.join(alert_message_parts)}"

        print(f"[MQTT] ⬆️ Publishing alert: {human_readable_payload}")
        try:
            mqtt_client.publish(MQTT_TOPIC_ALERTS, human_readable_payload)
        except Exception as e:
            print(f"[MQTT] ❌ Error publishing MQTT message: {e}")
    # Replace the current publish block with:
