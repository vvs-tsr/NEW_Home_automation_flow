MODEL_PATH = "yolov8n.pt"                     # YOLO model (e.g., yolov8n.pt, yolov8s.pt)
CONF_THRESHOLD = 0.4                          # Confidence threshold for detections
BUFFER_SIZE = 1                               # Number of frames to buffer for OpenCV (lower for less latency)

# MQTT Configuration (for sending YOLO alerts)
MQTT_BROKER = '51552d777a9f492cba1b3cc4ef6b37a9.s1.eu.hivemq.cloud' # Your HiveMQ Host
