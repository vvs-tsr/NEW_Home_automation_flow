last_alert_times = {} # Key: label (e.g., 'person'), Value: datetime object

# =========================
# SECURITY SENSITIVE OBJECT FILTER
# =========================
SECURITY_CLASSES = [
    'person',
    'bicycle', 'car', 'motorcycle', 'bus', 'truck', 'train', 'airplane', 'boat',
    'cat', 'dog', 'horse', 'sheep', 'cow', 'bear'
]

# =========================
# MQTT CLIENT SETUP
# =========================
mqtt_client = mqtt.Client(mqtt.CallbackAPIVersion.VERSION1, client_id=MQTT_CLIENT_ID)
mqtt_client.username_pw_set(MQTT_USERNAME, MQTT_PASSWORD)

def on_connect(client, userdata, flags, rc):
    if rc == 0:
        print(f"[MQTT] ✅ Connected to MQTT Broker: {MQTT_BROKER}")
    else:
        print(f"[MQTT] ❌ Failed to connect to MQTT Broker, return code {rc}")

try:
    mqtt_client.on_connect = on_connect
    mqtt_client.tls_set() # Enable TLS
    mqtt_client.connect(MQTT_BROKER, MQTT_PORT, 60)
    mqtt_client.loop_start()
except Exception as e:
    print(f"[MQTT] ❌ Error connecting to MQTT Broker: {e}")

# =========================
# LOAD YOLO MODEL
# =========================
try:
    model = YOLO(MODEL_PATH)
    print(f"[YOLO] ✅ Model '{MODEL_PATH}' loaded")

    # --- GPU USAGE ---
    if torch.cuda.is_available():
        model.to('cuda')
        print("[YOLO] Using CUDA (GPU) for inference. 🎉")
    else:
        model.to('cpu')
        print("[YOLO] CUDA not available, falling back to CPU for inference.")
    # --- END OF GPU USAGE ---

except Exception as e:
    print(f"[YOLO] ❌ Error loading YOLOv8 model: {e}")
    exit()

